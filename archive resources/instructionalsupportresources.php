    <h2>Instructional Datasets </h2>

    <p><a href="http://lib.stat.cmu.edu/DASL/"><strong>DASL</strong></a> is the Data and Story Library of materials that can be used to ilustrate the use of basic statistics methods. Use DASL's search engine to locate a wide variety of topics from real-world examples. </p>

    <p><a href="http://lib.stat.cmu.edu/datasets/"><strong>StatLib</strong></a> is maintained at Carnegie Mellon University. The alphabetical list of datasets from published and unpublished sources is one of the original sites of this type. </p>



    <p><a href="http://www.ats.ucla.edu/stat/stata/ado/teach/default.htm"><strong>STATA</strong></a> programs for teaching at UCLA  covers univariate distributions, probability simulation, statistical distributions, regression and other techniques. </p>

    <p><a href="http://robjhyndman.com/TSDL/"><strong>Time series data</strong></a> <span class="style2"><font size="-1">is a collection of about 800 time series drawn from many different fields including, agriculture, crime, demography, health, labor market and sports. It is compiled and maintained by Rob Hyndman. </font></span></p>

    

    <h2>Statistical Support </h2>

    <p><a href="http://www.ats.ucla.edu/stat/Schedule/default.htm"><strong>ATS</strong></a> provides statistical consulting at UCLA for the use of many different software packages including SPSS, SAS, STATA </p>



    <p><a href="http://www.dartmouth.edu/%7Echance/"><strong>Chance</strong></a> is a quantitative literacy course. The goal of Chance is to make students more informed, critical readers of current news stories that use probability and statistics. </p>

    <p><a href="http://statman.stat.sc.edu/rsrch/gasp/"><strong>GASP</strong></a> is an initiative designed to make statistical routines easily available via the Internet. The site contains lists of java applets for data analysis. The educational procedures are all written in Java and feature interactive graphics. </p>

    <p><a href="http://www.amstat.org/publications/jse/"><strong>JSE</strong></a> is an archive of statistical data supplied by the Journal of Statistics Education. JSE disseminates knowlede for the improvement of statistics education at all levels.    </p>

    <p><a href="http://www.businessbookmall.com/Statistics%20Internet%20Library.htm"><strong>Statistics Internet Library</strong></a> is probably more useful to students than faculty and has many tutorials, study aids, examples, practice problems as well as low cost live online tutors. </p>

    <h2>Software Resources </h2>

    <p><a href="http://maltman.hmdc.harvard.edu/socsci.shtml"><strong>Impoverished Social Scientist's Guide</strong></a> to Free Statistical Software and Resources contains free software for statistical packages, data visualization, data mining, spatial statistics and GIS, survey data collection and analysis, simulations, data cleaning, social network analysis, and more. </p>

<p><a href="http://www.ats.ucla.edu/stat/spss/default.htm"><strong>SPSS</strong></a> at UCLA is supported by a number of classes, online tutorials, analysis examples, and data management.</p>

    <p><a href="http://www.ats.ucla.edu/stat/sas/default.htm"><strong>SAS</strong></a> at UCLA is an online resource with tutorials, classes, examples, and links to online documentation.</p>

    <p><a href="http://www.sas.com/govedu/edu/programs/od_academics.html"><strong>SAS OnDemand for Academics</strong></a> &quot; allows students to download SAS on their own computers,  and perform statistical analysis

at their own convenience. Users register for the service, download a SAS software application via

the Web, and then perform processing by connecting to a hosted server

at SAS.&nbsp;Instructors connect to a secure Web site to register, create or update courses and download course software for free. Students then register and download software for the created course. By secure credit card transaction, students pay a per-course fee that is approximately the price of a textbook.&nbsp;&quot;</p>



    <p><a href="http://www.ats.ucla.edu/stat/stata/default.htm"><strong>STATA</strong></a> at UCLA provides support to researchers with online learning modules, classes, and a special section for <a href="http://www.ats.ucla.edu/stat/stata/ado/teach/default.htm">instructors</a>. </p>

    <p><a href="http://www.ssicentral.com/index.html"><strong>SSI</strong></a> is a company that creates software for statistical analysis. it is included here because the web site provides free downloads of software for LISREL, Hierarchical Linear Modeling (HLM) and Item response Theory (IRT). </p>

    

    

