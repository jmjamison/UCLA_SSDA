<p><a href="http://www.ssdan.net"><strong>SSDAN</strong></a> contains datasets and teaching exercises based on the United States Census. The Social Science Data Analysis Network is a university-based organization that creates user quides, web sites, and hands-on classroom computer materials. </p>

    <p><a href="http://www.census.gov/dmd/www/schoolessons.html"><strong>Teaching with the Census</strong></a> is a resource best used with lower division undergraduates. This site contains four lesson plans and  handouts for reading and writing about tables produced with U.S. Census data, performing simple calculations and creating maps.  </p>

