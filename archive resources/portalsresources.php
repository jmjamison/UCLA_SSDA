       <p> <a href="http://libraries.ucsd.edu/resources/data-gov-info-gis/ssds/guides/lij/index.html"><strong>Lijpart Elections  Archive</strong></a> The Lijphart Elections Archive is a static research collection  of district level election results for approximately 350 national legislative  elections in 26 countries that was maintained through 2003.</p><br>

                <a href="http://www.nber.org/data/"><strong>National Bureau of Economic Research Data Portal</strong></a> Provides access to a variety of data including macro level economic data, industry and international trade data, data on hospitals, vital statistics and demographic data, patents  and other assorted topics. Most studies are available in a variety of statistical package formats and are ready to download. NBER also provides access to the Current Population Surveys, <a href="http://www.nber.org/data/cps_basic.html">monthly</a> and <a href="http://www.nber.org/data/current-population-survey-data.html">supplemental files</a><br>



      </p>

      <p><a href="http://www.cpc.unc.edu/projects/addhealth"><strong>National Longitudinal Study of Adolescent Health (Add Health)</strong></a> &quot;is a longitudinal study of a nationally representative sample of   adolescents in grades 7-12 in the United States during the 1994-95   school year. The Add Health   cohort has been followed into young adulthood with four in-home   interviews, the most recent in 2008, when the sample was aged 24-32. Add Health   combines longitudinal survey data on respondents&rsquo; social, economic,   psychological and physical well-being with contextual data on the   family, neighborhood, community, school, friendships, peer groups, and   romantic relationships, providing unique opportunities to study how   social environments and behaviors in adolescence are linked to health and achievement outcomes in young adulthood. The fourth wave of interviews expanded the collection of biological data in Add Health to understand the social, behavioral, and biological linkages in health trajectories as the Add Health cohort ages through adulthood.&quot; (<em>from the Add Health website</em>) Data access is provided by <a href="http://www.icpsr.umich.edu">ICPSR</a> and <a href="http://www.socio.com">Sociometrics</a>.</p>



      <p><a href="http://www.pewinternet.org/data.asp"><strong>Pew 

        Internet and American Life Project </strong></a>Pew provide our raw data to scholars for their own research. This page contains links to the Project's survey  datasets that are currently available in SPSS on this site. Data is presented  in two forms: as an SPSS file and as cross tabulations of questions with  some basic demographics in a Word file. The site contains a search feature for identifying available data sets by topic. </p><br>

     

        <p><a href="http://www.ropercenter.uconn.edu/"><strong>Roper 

          Center for Public Opinion Research </strong></a> &quot;The Roper Center for Public Opinion Research is one of the world's leading archives of social science   data, specializing in data from surveys of public opinion. The data held  by the Roper Center range from the 1930s, when survey research was in   its infancy, to the present. Most of the data are from the United States,but over 50 nations are represented.&quot; UCLA students, faculty and  staff can search for entire polls or can search a question-level database  called <a href="http://www.ropercenter.uconn.edu/data_access/ipoll/ipoll.html">iPoll</a>.</p><br>

