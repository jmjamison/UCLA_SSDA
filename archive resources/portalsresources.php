<h1>Data Portals </h1>

<p><a href="http://electionstudies.org/"><strong>American National 

Election Studies</strong></a> On March 6, 2001, the Secretary of Commerce decided that unadjusted data from Census 2000 should be used to tabulate population counts reported to states and localities pursuant to 13 U.S.C. 141(c) (see 66 FR 14520, March 13, 2001). These numbers are not official Census 2000 counts. These numbers are estimates of the population based on a statistical adjustment method, utilizing sampling and modeling, applied to the official Census 2000 figures.</p>

    <p><br>

<a href="http://www.measuredhs.com/Data/"><strong>Demographic  and Health Surveys</strong></a> DHS surveys provide data for a wide range of monitoring and impact evaluation indicators in the areas of population, health, and nutrition.</p>

    <p><br>

      <a href="../marvick_pubs.htm"><strong>Dwaine Marvick Research Collection</strong></a> Dwaine Marvick, 1923-1999 was a Political Science Professor at UCLA. He conducted research projects into attitudes and activities at several levels of politics in California, West Africa, India and Germany. This portal is a record of his publications, data sets and other materials. Please contact the <a href="mailto:libbie@ucla.edu">Data Archive</a> for more information about datasets.</p> <br>

 

    <p><a href="../earthquake/erthqkstudies2.index.htm"><strong>Earthquake Survey Data from UCLA </strong></a> Earthquake related behavior is an important part of disaster research in California. Behavior during and immediately after earthquakes is influenced by a range of socioeconomic, situational, and social psychological factors. Here you will find survey data collected as part of research at the University of California , Los Angeles about the knowledge, attitudes and behaviors of individuals in responding to earthquakes.<br>

</p>

     



      <p><a href="http://www.norc.org/GSS+Website/"><strong>General Social Survey</strong></a> &nbsp;&quot;The General Social Survey (GSS) conducts basic scientific research   on the   structure and development of American society with a   data-collection program   designed to both monitor social change within   the United   States and to compare the   United States to other     nations. <span id="hpcContent">The GSS contains a standard 'core' of demographic,   behavioral,&nbsp;and attitudinal questions, plus topics of special interest.   Many of the core questions have remained unchanged since 1972 to   facilitate time-trend studies as well as replication of earlier   findings.&quot; (<em>from the GSS website</em>)</span> Data access is provided through the <a href="http://www.norc.org/GSS+Website/">GSS website</a> and <a href="http://www.icpsr.umich.edu">ICPSR</a>.</p><br>



          <p><a href="http://hrsonline.isr.umich.edu/"><strong>Health and Retirement Study</strong></a> &nbsp;&quot;The Health and Retirement Study (HRS) is a source of longitudinal, cross-sectional data for researchers and policymakers who study aging. HRS surveys more than 22,000 Americans over the age of 50 every two years and studies the labor force participation and health transitions that individuals undergo toward the end of their work lives and in the years that follow. The study has collected information about income, work, assets, pension plans, health insurance, disability, physical health and functioning, cognitive functioning, and health care expenditures.&quot; (<em>from the HRS website</em>) Data access is availalbe from the <a href="http://hrsonline.isr.umich.edu/">HRS website</a> and <a href="http://www.icpsr.umich.edu">ICPSR</a>.</p>

      <p><a href="http://usa.ipums.org/usa/"><strong>IPUMS USA</strong></a> This is a project dedicated to collecting and distributing United States census data. The resources here allow you to make subsets of US Census 

 data across censuses and for selections of variables or geography. &quot;The Integrated Public Use Microdata Series (IPUMS) consists of thirty-nine high-precision samples of the American population drawn from fifteen federalcensuses and from the American Community Surveys of 2000-2006. Some of these samples have existed for years, and others were created specifically for this database. The thirty-nine samples, which draw on every surviving census from 1850-2000, and the 2000-2006 ACS samples, collectively comprise our richest source of quantitative information on long-term changes in the American population.&quot;</p>



      <p><a href="https://international.ipums.org/international/"><strong>IPUMS  International</strong></a> As with the IPUMS USA this project aims to make available census data from countries around the world. Census files are currently available for 35 countries. </p>

      

      <p><a href="http://www.issp.org/"><strong>International Social Survey Program (ISSP)</strong></a> The ISSP is a continuing annual program of cross-national   collaboration on surveys covering topics important for social science   research. <a href="http://www.gesis.org/en/institute/">GESIS Data Archive and Data Analysis</a>, the ISSP data archive, is responsible for   archiving, integrating data and documentation and for the distribution   of the merged international datasets. Topics in the ISSP largely focus on Role of Government, Social Networks, Social Inequality, Family and Changing Gender Roles, Work Orientations, Religion, Environment, National Identity, Citizenship and Leisure Time and Sports. There are 45 member countries participating in the ISSP. <br>

