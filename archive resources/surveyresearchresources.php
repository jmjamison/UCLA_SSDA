<h1>Survey Research Resources</h1>        
     
  <blockquote>
      <p><strong><a href="http://www.methodspace.com/">Methodspace</a></strong> This is essentialy a social network and blog for  those involved in survey research using a variety of methods; quantitative,  qualitative, mixed, etc. It is a place to learn about survey research methods  events and to read journal articles. Requires a registration. </p>
      <p><strong><a href="http://surveynet.ac.uk/">Survey Resources Network</a></strong> &quot;The aim of the Network is to  provide a coherent and integrated approach to the development of skills and  knowledge, the dissemination of research findings and research resources, and  the promotion of best survey practice.&quot; (from the SNR website) The site  contans links to question banks, resources for gaining survey skills, resources  on survey design and methods, and ongoing research on survey design methods.</p>

      <p>American Association for Public Opinion Research <strong><a href="http://www.aapor.org/bestpractices">Best  Practices for Survey and Public Opinion Research</a></strong>. Provides and overview on the  best practices for carrying out quality surveys. Includes topics on survey  goals, sampling, design, question wording, pretesting, interviewer training,  quality checks, response rates, analysis techniques, privacy and  confidentiality, and disclosure of survey methods.</p>
      <p><strong><a href="http://www.ropercenter.uconn.edu/education/polling_fundamentals.html">Roper Center Polling 101</a></strong> &quot;This tutorial is intended to  offer a simplified glimpse into some of the fundamentals of public opinion  polling. Designed for the novice, POLLING 101 provides definitions, examples,  and explanations that serve to introduce interested students to the field of  public opinion research.&quot; (From the Roper website)</p>
      <p><strong>American Statistical Association</strong>.<strong><a href="http://www.whatisasurvey.info/"> What is a Survey?</a></strong> by Fritz  Scheuren. &quot;This &quot;What is a Survey&quot; booklet is written primarily  for non-specialists and is free of charge. Its overall goal is to improve  survey literacy among individuals who participate in NORC Surveys or use NORC  survey results.&quot; To download a copy, <a href="../index/What_is_a_Survey.pdf">click here</a>.</p>

      <p><strong> <a href="http://catalog.library.ucla.edu/cgi-bin/Pwebrecon.cgi?v1=3&ti=1,3&Search_Arg=the%20survey%20kit&Search_Code=TKEY^&SL=None&CNT=50&PID=u19fh48ksIEC2jt4AmPuriYWt5aZfRE&SEQ=20120305143051&SID=2"> The Survey Kit</a></strong>. Arlene Fink, ed. The Survey Kit contains 9  volumes covering all aspects of survey research. Volume titles include: The  Survey Handbook, How to Ask Survey Questions, How to Conduct Self-administered  and Mail Surveys, How to Conduct Interviews by Telephone and In-person, How to  Design Surveys, How to Sample in Surveys, How to Measure Survey Reliability and  Validity, How to Analyze Survey Data, and How to Report on Surveys. These volumes can be found in most university libraries.</p>
