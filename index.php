  <h1>About the Data Archive</h1>



<P>Data services have been operating at UCLA since  1961 when political   science professor Dwaine Marvick established the Political  Behavior   Archive (PBA); through his efforts, UCLA was one of the 8 founding    members of the Inter-university Consortium for Political and Social   Research (ICPSR)  in 1962.&nbsp; When the Survey Research Center  (SRC) was   formed in 1964,under the direction of Eve Fielder, the PBA was merged   with a planned Archive Data  Library the SRC was to house.&nbsp;In 1977, Donald Treiman (Distinguished Professor, Sociology) was instrumental in designing and defining a newframework for the Data Archive.   Under the guidance and stewardship of William A.V. Clark (Professor, Geography) the Archive developed into the popular campus facility of today.</P>


<p>
The mission  of the Social Science Data Archive has been and   continues to be to provide a foundation for social  science research   with faculty support throughout an entire research project  involving   original data collection or the reuse of publicly available studies.   Data Archive staff and researchers work as partners throughout all   stages of the research process, beginning when a hypothesis or area of   study is  being developed, during grant and funding activities, while   data  collection and/or analysis is ongoing, and finally in long term   preservation of research  results. Our role is to provide a    collaborative environment where the focus is on understanding the nature   and scope of research  approach and management of research output   throughout  the entire life cycle of the project. </p>


<p>
Instructional support, especially support that  links research with   instruction is also a mainstay of   operations.&nbsp; The Data Archive is one   of  the key support components in those disciplines that provide   instruction in  research methods, survey design and methods, statistics,   and data analysis.  Throughout the campus, data provided by the Archive   is used by faculty in  courses to transmit pedagogy, illustrate theory   and develop understanding of  social, political, demographic, economic,   geographic and historical trends and  issues.</P>




